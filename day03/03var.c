/*
 变量练习
 */
#include <stdio.h>
int main(){
   int num = 1;
   printf("%d X %d = %d\n", num, 10 - num, num * (10 - num));
   num = 2;
   printf("%d X %d = %d\n", num, 10 - num, num * (10 - num));
   num = 3;
   printf("%d X %d = %d\n", num, 10 - num, num * (10 - num));
   num = 4;
   printf("%d X %d = %d\n", num, 10 - num, num * (10 - num));
   num = 5;
   printf("%d X %d = %d\n", num, 10 - num, num * (10 - num));
   return 0;
}
